package logica;

public class Equipo {

	private String nombre;
	private Boolean lider;
	public Equipo() {
		super();
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public Boolean getLider() {
		return lider;
	}
	public void setLider(Boolean lider) {
		this.lider = lider;
	}
	
	
}
